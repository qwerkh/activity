export const Sch_Promotion = new Mongo.Collection('sch_promotion');

Sch_Promotion.schema = new SimpleSchema({
    name: {
        type: String,
        label: "Name"
    },
    khName: {
        type: String,
        label: "Khmer",
        optional: true
    },
    promotionType: {
        type: String
    },
    desc: {
        type: String,
        optional: true
    },
    value: {
        type: Number,
        decimal: true
    },
    status: {
        type: Boolean
    },
    rolesArea: {
        type: String,
        label: "Role Area",
        optional: true
    },
    createdAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return moment().toDate();
            }
        }
    },
    updatedAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return moment().toDate();
            }
        }
    },
    createdUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return Meteor.userId();
            }
        }
    },
    updatedUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return Meteor.userId();
            }
        }
    }
});

Sch_Promotion.attachSchema(Sch_Promotion.schema);

