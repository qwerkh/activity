export const Sch_Transfer = new Mongo.Collection('sch_transfer');

Sch_Transfer.schema = new SimpleSchema({
    lekBrokas: {
        type: String,
        optional: true
    },
    desc: {
        type: String,
        optional: true
    },
    teacherId: {
        type: String
    },
    date: {
        type: Date
    },
    rolesArea: {
        type: String,
        label: "Role Area",
        index: true,
        optional: true
    },
    createdAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return moment().toDate();
            }
        },
        index: true
    },
    updatedAt: {
        type: Date,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return moment().toDate();
            }
        }
    },
    createdUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isInsert) {
                return Meteor.userId();
            }
        }
    },
    updatedUser: {
        type: String,
        optional: true,

        autoValue() {
            if (this.isUpdate) {
                return Meteor.userId();
            }
        }
    }
});

Sch_Transfer.attachSchema(Sch_Transfer.schema);

